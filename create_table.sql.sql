CREATE TABLE `financial` ( 
  `id` INT AUTO_INCREMENT NOT NULL,
  `symbol` VARCHAR(100) NULL DEFAULT NULL ,
  `totalAsset` DOUBLE NULL DEFAULT NULL ,
  `totalLiability` DOUBLE NULL DEFAULT NULL ,
  `equity` DOUBLE NULL DEFAULT NULL ,
  `totalRevenue` DOUBLE NULL DEFAULT NULL ,
  `netProfit` DOUBLE NULL DEFAULT NULL ,
  `eps` FLOAT NULL DEFAULT NULL ,
  `year` VARCHAR(50) NULL DEFAULT NULL ,
  `netProfitMargin` DOUBLE NULL DEFAULT NULL ,
  `roa` DOUBLE NULL DEFAULT NULL ,
  `roe` DOUBLE NULL DEFAULT NULL ,
  CONSTRAINT `PRIMARY` PRIMARY KEY (`id`)
);
CREATE TABLE `yeild_stock` ( 
  `symbol` VARCHAR(15) NULL DEFAULT NULL ,
  `type` VARCHAR(12) NULL DEFAULT NULL ,
  `bookCloseDate` VARCHAR(25) NULL DEFAULT NULL ,
  `paymentDate` VARCHAR(25) NULL DEFAULT NULL ,
  `returnAmount` DECIMAL(15,8) NULL DEFAULT NULL ,
  `xdate` VARCHAR(50) NULL DEFAULT NULL ,
  `dividend` DECIMAL(15,8) NULL DEFAULT NULL 
);
CREATE TABLE `yeild_stock_lastprice` ( 
  `symbol` VARCHAR(50) NULL DEFAULT NULL ,
  `price` DECIMAL(20,6) NULL DEFAULT NULL 
);
CREATE TABLE `yeild_period_2` ( 
  `id` BIGINT NOT NULL,
  `yeild_date` DATE NULL DEFAULT NULL ,
  `yeild` FLOAT NULL DEFAULT NULL ,
  `percent_yeild` FLOAT NULL DEFAULT NULL ,
  `symbol` VARCHAR(300) NULL DEFAULT NULL 
);
